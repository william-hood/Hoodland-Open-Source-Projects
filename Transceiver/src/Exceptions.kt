package rockabilly.transceiver

class IllegalStatusCodeException(code: Int) : Exception("Status code $code is outside the valid range of 100 to 599.")

class HttpMessageParseException : Exception {
    constructor(cause: Exception?) : this("Unable to parse an incoming HTTP message.", cause) {}
    constructor(message: String?, cause: Exception?) : super(message, cause) {}
    constructor(message: String?) : super(message) {}
}